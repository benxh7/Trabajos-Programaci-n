    /**
     * Metodo para mostrar el registro de pagos realizados por un socio,
     * si el socio tiene registros, se muestra en un mensaje, si no, se informa que no tiene pagos.
     */
    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // Obtenemos el RUT y le damos formato
        String rutIngresado = txtRut.getText().trim();
        String rutFormateado = Utils.formatearRut(rutIngresado);

        if (rutFormateado == null) {
            Mensajes.mostrarError(this, Mensajes.ERROR_RUT);
            return;
        }

        SocioManager socio = regSocioManager.buscarSocioPorRut(rutFormateado);
        if (socio == null) {
            Mensajes.mostrarError(this, Mensajes.SOCIO_NO_ENCONTRADO);
            return;
        }

        List<Pagos> pagos = regSocioManager.obtenerPagos(socio.getNumDeSocio());
        if (pagos.isEmpty()) {
            Mensajes.mostrarInfo(this, "El socio no tiene registros de pagos.");
        } else {
            StringBuilder registro = new StringBuilder("Registros de pagos:\n");
            for (Pagos pago : pagos) {
                registro.append(String.format("[%s] Número de Socio: %d Monto: %s\n",
                        pago.getFechaFormateada(),
                        socio.getNumDeSocio(),
                        Utils.formatearDinero(pago.getMonto())
                    )
                );
            }
            JOptionPane.showMessageDialog(this, registro.toString(), "Registro de Pagos", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistroActionPerformed

