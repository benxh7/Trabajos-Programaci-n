    /**
     * Este metodo busca el monto total cancelado por un socio, tambien
     * se encarga de validar el RUT ingresado y muestra el monto total cancelado.
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Obtenemos el RUT ingresado
        String rutIngresado = txtRut.getText().trim();
        
        // Dar formato al RUT del Socio
        String rutFormateado = Utils.formatearRut(rutIngresado);
        if (rutFormateado == null) {
            // Mostramos el mensaje de error si el RUT es invalido
            Mensajes.mostrarError(this, Mensajes.ERROR_RUT);
            return;
        }

        // Buscar socio por RUT
        SocioManager socio = regSocioManager.buscarSocioPorRut(rutFormateado);
        if (socio == null) {
            Mensajes.mostrarError(this, Mensajes.SOCIO_NO_ENCONTRADO);
        } else {
            // Mostrar el monto total cancelado
            txtMontoTotalCancelado.setText(String.valueOf(socio.getCantidadAportada()));
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

